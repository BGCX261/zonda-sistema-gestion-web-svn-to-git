<h2 style="text-align: right">Preguntas frecuentes</h2>
<hr>
<br />
<h2>C&oacute;mo doy de alta un registro?</h2>
<p>
    Haga click en la opci&oacute;n <span style="font-weight: bold">nuevo</span> del men&uacute; que se encuentra a la izquierda de la pantalla. Se abrir&aacute; un di&aacute;logo en el cual se ingresan los datos relativos al registro por agregar. Si hay alg&uacute;n error en el ingreso de los datos, el sistema informa del mismo con un mensaje en la parte superior del formulario y resaltando el registro a modificar. A continuaci&oacute;n, presione el boto&oacute;n <span style="font-weight: bold">aceptar</span> y el registro se agregar&aacute; autom&aacute;ticamente en la base de datos.
</p>
<br />
<h2>C&oacute;mo edito la informaci&oacute;n de un registro?</h2>
<p>
    En cada listado, en el final de cada fila, se encuentra la opci&oacute;n <span style="font-weight: bold">editar</span>. Al hacer click sobre ella se abrir&aacute; un di&aacute;logo an&aacute;logo al de ingreso de un nuevo registro. Edite la informaci&oacute;n del formulario y haga click en <span style="font-weight: bold">aceptar</span> para guardar los cambios a la base de datos.
</p>
<br />
<h2>C&oacute;mo elimino un registro?</h2>
<p>
    Al igual que para la acci&oacute;n de editar, en cada fila de los listados se encuentra la opci&oacute;n <span style="font-weight: bold">borrar</span>. Al hacer click sobre ella se abrir&aacute; un di&aacute;logo de confirmaci&oacute;n de borrado. Haga click en <span style="font-weight: bold">aceptar</span> para eliminar permanentemente el registro de la base de datos.
</p>
<br />
<h2>C&oacute;mo busco un registro?</h2>
<p>
    Cada listado tiene toda la informaci&oacute;n relacionada con los registros del sistema. Al acceder a un listado se puede navegar por dicha informaci&oacute;n. Utilice los botones en la base del listado para moverse a trav&eacute;s de las p&aacute;ginas del mismo. Utilice el filtro ubicado arriba del listado para buscar registros con alg&uacute;n criterio espec&iacute;fico. Seleccione del combo de b&uacute;squeda el campo por el cual buscar y escriba el par&aacute;metro de b&uacute;squeda a continuaci&oacute;n. Autom&aacute;ticamente se actualizar&aacute; el listado en base al texto ingresado.
    <br/>
    El encabezado de cada columna puede utilizarse para ordenar los registros en forma ascendente o descendente, simplemente haciendo click sobre &eacute;l.
</p>
<br />
<h2>C&oacute;mo se administran los precios?</h2>
<p>
    Los precios se manejan en base a listas de precios. En la secci&oacute;n <span style="font-weight: bold">art&iacute;culos</span> se encuentra el administrador de listas de precios. Cree una nueva lista de precios. A continuaci&oacute;n haga click en la opci&oacute;n del men&uacute; <span style="font-weight: bold">precios</span>. Se abrir&aacute; un di&aacute;logo de modificaci&oacute;n de precios. Indique en el primer combo la lista reci&eacute;n creada. Luego ajuste los par&aacute;metros del formulario para aplicar el precio deseado. Haga click en <span style="font-weight: bold">aceptar</span> y los cambios se guardar&aacute;n en la lista de precios reci&eacute;n creada. 
    <br />
    Para navegar por los precios reci&eacute;n modificados, ingrese nuevamente en el listado de listas de precios, seleccione la lista de precios deseada haciendo click sobre la fila de la tabla, y se podr&aacute;n ver todos los precios de esa lista. All&iacute; pueden modificarse los precios de forma individual.
    <br />
    Por &uacute;ltimo, en el men&uacute; de la secci&oacute;n <span style="font-weight: bold">clientes</span> seleccione la opci&oacute;n <span style="font-weight: bold">listas de precios</span>, y a continuaci&oacute;n en el bot&oacute;n <span style="font-weight: bold">asociar una lista a un cliente</span>. En el di&aacute;logo que se abrir&aacute; indique el cliente y la lista que quieren relacionarse, y haga click en <span style="font-weight: bold">aceptar</span> para guardar los cambios.
</p>
<br />
<h2>C&oacute;mo se administran los costos?</h2>
<p>
    Los costos se calculan en base a los costos de los art&iacute;culos ingresados en las compras realizadas. Seleccione la opci&oacute;n <span style="font-weight: bold">costos</span> de la secci&oacute;n <span style="font-weight: bold">art&iacute;culos</span> para elegir de que manera se administrar&aacute;n los costos actuales.
</p>
<br />
<h2>C&oacute;mo se realiza una compra?</h2>
<p>
    En la secci&oacute;n <span style="font-weight: bold">compras</span> seleccione la opci&oacute;n <span style="font-weight: bold">nueva</span>. Seleccione el tipo de operaci&oacute;n a realizar (la cual debe haber sido ingresada desde la opci&oacute;n <span style="font-weight: bold">tipos de factura</span> de la secci&oacute;n <span style="font-weight: bold">configuraci&oacute;n</span>), ingrese los datos de la factura desde el bot&oacute;n <span style="font-weight: bold">cambiar datos</span>, y luego ingrese los art&iacute;culos comprados haciendo click en el bot&oacute;n <span style="font-weight: bold">agregar art&iacute;culo</span>. 
    <br />
    Una vez que haya ingresado todos los datos, haga click en el bot&oacute;n <span style="font-weight: bold">grabar compra</span>. Los datos se almacenar&aacute;n de forma permanente en la base de datos.
</p>
<br />
<h2>C&oacute;mo se realiza una venta?</h2>
<p>
    En la secci&oacute;n <span style="font-weight: bold">ventas</span> seleccione la opci&oacute;n <span style="font-weight: bold">nueva venta</span>. Luego proceda de forma similar a la descripta durante la realizaci&oacute;n de una compra.
</p>
<br />
<h2>C&oacute;mo realizo un pedido?</h2>
<p>
    En la secci&oacute;n <span style="font-weight: bold">ventas</span> seleccione la opci&oacute;n <span style="font-weight: bold">nuevo pedido</span>. Luego proceda de forma similar a la descripta durante la realizaci&oacute;n de una compra. 
    <br />
    Luego podr&aacute; ver, exportar a una planilla de c&aacute;lculo, o imprimir los pedidos ingresados desde la opci&oacute;n <span style="font-weight: bold">listado de pedidos</span>. Para poder procesar un pedido o cancelarlo seleccione la opci&oacute;n <span style="font-weight: bold">administrar pedidos</span>. Al procesar un pedido, &eacute;ste se cargar&aacute; como una venta y estar&aacute; en condiciones de ser editado o grabado como venta.
</p>
<br />
<h2>C&oacute;mo imprimo una factura?</h2>
<p>
    En ambas secciones, <span style="font-weight: bold">compras</span> o <span style="font-weight: bold">ventas</span>, seleccione la opci&oacute;n <span style="font-weight: bold">listado de facturas</span>. Al final de cada fila encontrar&aacute; la opci&oacute;n <span style="font-weight: bold">ver</span>, la cual permitir&aacute; ver la factura en pantalla. All&iacute; encontrar&aacute; los botones <span style="font-weight: bold">imprimir</span> y <span style="font-weight: bold">exportar</span>.
</p>
<br />
<h2>C&oacute;mo se numeran las facturas de venta?</h2>
<p>
    En la opci&oacute;n <span style="font-weight: bold">talonarios</span> de la secci&oacute;n <span style="font-weight: bold">ventas</span> se encuentra la administraci&oacute;n de los talonarios de facturas. All&iacute; se puede agregra un talonario, indicar su numeraci&oacute;n inicial y actual, modificar los datos del talonario y darlo de baja.
</p>
<br />
<h2>C&oacute;mo se controla el estado de las facturas de compra y de venta?</h2>
<p>
    En las secciones <span style="font-weight: bold">clientes</span> y <span style="font-weight: bold">proveedores</span> se encuentra la opci&oacute;n <span style="font-weight: bold">facturas pendientes</span>, la cual muestra en pantalla todas las facturas que no se han cancelado todav&iacute;a. Al final de cada fila se encuentra la opci&oacute;n de ingresar el pago de dicha factura. El pago puede realizarse por el total o por una parte del monto de la factura.
</p>
<br />
<h2>C&oacute;mo se controlan las ventas en cuotas?</h2>
<p>
    En el caso de una venta realizada en cuotas, en el men&uacute; de la secci&oacute;n <span style="font-weight: bold">clientes</span> se encuentra la opci&oacute;n <span style="font-weight: bold">agenda de pagos</span>, la cual muestra una lista con todos los pagos pendientes de cada cliente para cada factura emitida a pagar en cuotas. Al final de la fila se encuentra la opci&oacute;n <span style="font-weight: bold">ingresar pago</span>, la cual muestra un di&aacute;logo para el ingreso del pago de una cuota. Al hacer click en el bot&oacute;n <span style="font-weight: bold">aceptar</span> se ingresar&aacute; el pago a la base de datos.
</p>
<br />
<h2>C&oacute;mo se administran los fondos?</h2>
<p>
    En la secci&oacute;n <span style="font-weight: bold">fondos</span> pueden administrarse los registros de fondos. La opci&oacute;n <span style="font-weight: bold">nuevo</span> agrega un registro de fondo. En la opci&oacute;n <span style="font-weight: bold">listado</span> pueden editarse o eliminarse los fondos. El &iacute;tem <span style="font-weight: bold">seleccionar fondo de caja</span> permite indicar qu&eacute; registro de fondo actuar&aacute; como la caja diaria de la empresa.
</p>
<br />
<h2>C&oacute;mo se controlan los movimientos de los usuarios?</h2>
<p>
    En la secci&oacute;n <span style="font-weight: bold">configuraci&oacute;n</span> est&aacute;n las herramientas necesarias para esta tarea. La opci&oacute;n <span style="font-weight: bold">movimientos de usuarios</span> muestra toda la informaci&oacute;n relativa a las acciones de los usuarios dentro del sistema. La opci&oacute;n <span style="font-weight: bold">ingreso de usuarios</span> muestra en qu&eacute; momento y desde cu&aacute;l direcci&oacute;n se han conectado los usuarios del sistema. 
</p>
<br />
<h2>C&oacute;mo se respalda la base de datos?</h2>
<p>
    En la secci&oacute;n <span style="font-weight: bold">configuraci&oacute;n</span> se encuentra la opci&oacute;n <span style="font-weight: bold">respaldo de datos</span>. La misma genera un archivo con toda la informaci&oacute;n contenida en la base de datos hasta ese momento. Para poder recuperar dicha informaci&oacute;n, haga click en la opci&oacute;n <span style="font-weight: bold">recuperaci&oacute;n de datos</span>. La misma abrir&aacute; una pantalla que permitir&aacute; cargar el archivo generado anteriormente a la base de datos.
    <br />
    <span style="font-weight: bold">Importante:</span> Toda la informaci&oacute;n contenida en la base ser&aacute; reemplazada por la del archivo cargado. Si en la base de datos hubiera informaci&oacute;n m&aacute;s reciente esta se perder&aacute; de forma permanente.
</p>
<br />
<h2>La base de datos puede exportarse?</h2>
<p>
    Si. En la secci&oacute;n <span style="font-weight: bold">configuraci&oacute;n</span> se encuentra la opci&oacute;n <span style="font-weight: bold">exportar base de datos</span>. La misma permite generar un archivo con extensi&oacute;n sql que contiene toda la informaci&oacute;n y la estructura de la base de datos. Este archivo puede utilizarse como un respaldo de datos o como una r&eacute;plica de la base de datos actual.
</p>
