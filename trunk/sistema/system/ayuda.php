<h1>Ayuda del sistema</h1>
<br />
<h2 style="text-align: right">Secciones del sistema</h2>
<hr>
<br />
<h2>Art&iacute;culos</h2>
<p>
    <img src="images/articulos-128x128.png" /><br />
    Contiene todas las operaciones relacionadas con el manejo de los art&iacute;culos: stock, precios, costos, y categor&iacute;as de art&iacute;culos.
    <br />
    Desde aqu&iacute; puede agregar, editar o eliminar registros de art&iacute;culos.
</p>
<br />
<h2>Compras</h2>
<p>
    <img src="images/compras-128x128.png" /><br />
    Permite realizar compras en el sistema. Las compras modifican la existencia de art&iacute;culos en el stock, el saldo de cuenta de los proveedores y el saldo de la empresa.
    <br />
    Desde el listado de compras pueden verse, imprimirse o exportar las facturaciones realizadas y llevar un control de las mismas.
</p>
<br />
<h2>Ventas</h2>
<p>
    <img src="images/ventas-128x128.png" /><br />
    Permite realizar ventas en el sistema. Las ventas modifican la existencia de art&iacute;culos en el stock, el saldo de cuenta de los clientes y el saldo de la empresa.
    <br />
    Aqu&iacute; pueden tomarse los pedidos de clientes y administrar los mismos. 
    <br />
    Desde el listado de facturas pueden verse, imprimirse o exportar las facturaciones realizadas y llevar un control de las mismas.
    <br />
    Tambi&eacute;n pueden anularse las facturas realizadas y volver los estados de cuenta y el stock de art&iacute;culos al estado anterior a la venta.
</p>
<br />
<h2>Clientes</h2>
<p>
    <img src="images/clientes-128x128.png" /><br />
    Aqu&iacute; se encuentra la administraci&oacute;n de los registros de clientes. Alta, baja, y edici&oacute;n de registros.
    <br />
    Tambi&eacute;n est&aacute; el listado de las facturas por cobrar a los clientes, y el listado de pagos pendientes para las ventas realizadas en cuotas. Desde aqu&iacute; se pueden ingresar los pagos realizados por los clientes.
</p>
<br />
<h2>Proveedores</h2>
<p>
    <img src="images/proveedores-128x128.png" /><br />
    Aqu&iacute; se encuentra la administraci&oacute;n de los registros de proveedores. Alta, baja, y edici&oacute;n de registros. Es similar al manejo de los registros de clientes.
    <br />
    Tambi&eacute;n est&aacute; el listado de las facturas por pagar a los proveedores, desde donde pueden realizarse los pagos de dichas facturas.
</p>
<br />
<h2>Fondos</h2>
<p>
    <img src="images/fondos-128x128.png" /><br />
    Administraci&oacute;n de los fondos de la empresa. Se pueden agregar nuevos fondos, editarlos o eliminarlos. Aqu&iacute; se selecciona el fondo que actuar&aacute; como la caja diaria.
    <br />
    Adem&aacute;s pueden realizarse las notas de cr&eacute;dito y d&eacute;bito y administrar las mismas.
</p>
<br />
<h2>Configuraci&oacute;n</h2>
<p>
    <img src="images/configuracion-128x128.png" /><br />
    Desde aqu&iacute; se realiza la configuraci&oacute;n de los distintos par&aacute;metros del sistema: administraci&oacute;n de localidades, tipos de al&iacute;cuotas, y tipos de facturas con las cuales operar.
    <br />
    Toda la informaci&oacute;n de los usuarios del sistema se encuentra en esta secci&oacute;n. Aqu&iacute; pueden administrarse los usuarios y controlar sus movimientos.
    <br />
    El respaldo y la recuperaci&oacute;n de la informaci&oacute;n almacenada en la base de datos puede realizarse desde aqu&iacute;.
    <br />
    Tambi&eacute;n puede cambiarse el dise&ntilde;o de la pantalla y la informaci&oacute;n personal de su usuario. 
</p>
<br />
<h2>Informes</h2>
<p>
    <img src="images/informes-128x128.png" /><br />
    En esta secci&oacute;n se encuentran todos los informes que puede generar el sistema. Se encuentran agrupados las mismas categor&iacute;as que se han descripto anteriormente. 
    <br />
    Los informes son del tipo listado o gr&aacute;fico. Para las ventas y las compras hay res&uacute;menes de las actividades en un periodo de tiempo determinado.
    <br />
    Todos los informes pueden imprimirse o exportarse a una planilla de c&aacute;lculo.
</p>
